
public class Date {
	int Month;
	int Day;
	int Year;

	public Date(int month, int day, int year) {
		Month = month;
		Day = day;
		Year = year;

	}

	public int getMonth() {
		return Month;
	}

	public void setMonth(int month) {
		Month = month;
	}

	public int getDay() {
		return Day;
	}

	public void setDay(int day) {
		Day = day;
	}

	public int getYear() {
		return Year;
	}

	public void setYear(int year) {
		Year = year;
	}

}
